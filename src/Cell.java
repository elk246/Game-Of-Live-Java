public class Cell {

   private boolean isAlive= false;
   private boolean isStone= false;

    //Getter
    public boolean isAlive() {
        return isAlive;
    }

    //setter
    public void setAlive(boolean alive) {
        isAlive = alive;
    }



    // getter stone
    public boolean isStone() {
        return isStone;
    }

    //setter stone
    public void setStone(boolean stone) {
        isStone = stone;
    }
}


